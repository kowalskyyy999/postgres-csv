    ID INT,
    ARTISTS_NAME VARCHAR,
    TRACK_NAME VARCHAR,
    TRACK_ID VARCHAR,
    POPULARITY INT,
    YEAR INT,
    GENRE VARCHAR,
    DANCEABILITY FLOAT,
    ENERGY FLOAT,
    KEY INT,
    LOUDNESS FLOAT,
    MODE INT,
    SPEECHINESS FLOAT,
    ACOUSTICNESS FLOAT,
    INSTRUMENTALNESS FLOAT,
    LIVENESS FLOAT,
    VALENCE FLOAT,
    TEMPO FLOAT,
    DURATION_MS INT,
    TIME_SIGNATURE INT
)